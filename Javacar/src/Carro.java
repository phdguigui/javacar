public class Carro extends Veiculo {
	private double litragem;
	private int portas;
	
	public double getLitragem() {
		return litragem;
	}

	public void setLitragem(double litragemMotor) {
		this.litragem = litragemMotor;
	}

	public int getPortas() {
		return portas;
	}

	public void setPortas(int portas) {
		this.portas = portas;
	}
	
}
